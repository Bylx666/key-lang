//! 映射表

/// 返回符号优先级
pub fn prec(x:&[u8])-> u8 {
  match x {
    b"-."|b"-:" => 14,
    b"::"|b"->" => 13,
    b"("|b"[" => 12, // 代指调用和索引
    b"*" | b"%" | b"/" => 11, 
    b"+" | b"-" => 10, 
    b"<<"|b">>" => 9,
    b"&" => 8,
    b"^" => 7,
    b"|" => 6,
    b"=="|b"!="|b"<"|b">"|b"<="|b">=" => 5,
    b"&&" => 4,
    b"||" => 3,
    b"="|b"+="|b"-="|b"*="|b"/="|b"%="|b"&="|b"|="|b"^="|b"<<="|b">>=" => 2,
    _=> 0
  }
}


/// 转义符表
pub fn escape(c:u8)-> u8 {
  match c {
    b'n'=> b'\n',
    b'r'=> b'\r',
    b't'=> b'\t',
    b'\\'=> b'\\',
    b'0'=> 0,
    _=> 255
  }
}

